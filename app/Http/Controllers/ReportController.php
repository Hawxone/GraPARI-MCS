     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {

        $check = DB::table('checks')
            ->orderBy('created_at', 'DESC')->get();

        $perangkat = DB::table('perangkats')->get();

        $ws = array();

        foreach ($perangkat as $p) {
            $ws['perangkat'] = DB::table('checks')
                ->where('nama_checklist','==',$p->nama_perangkat)
                ->get();
        }




        return view('pages.report.index')->with('check',$check)->with('data',$ws);
    }

